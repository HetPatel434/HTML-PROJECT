     <div class="PAGE-1-Typesofradiobroadcasting">
        <h2>Types Of Radio Broadcasting</h2></div> 
        
        </div>
       <div class="PAGE-1-Tyorbp">
        <p><h4>
        (1)AM- AM (amplitude modulation) was the earliest form of radio broadcasting. This specific type of broadcasting got 
               it’s name for how it emits radio waves by manipulating the amplitude of the signal in conjunction with the amplitude
               of the signal that is being transmitted. What is known as a ‘medium-wave band’ is what is used worldwide to deliver AM 
               broadcasting across the globe, while other parts of the world (like Europe) also use ‘long-wave band’ broadcasting. 
               A great advantage to AM radio is that is fairly simplistic and the sound that is emitted from this type of signal can be easily detected.
        </h4></p>
        <p><h4>
        (2)FM- FM radio (frequency modulation) was invented in the 1930s as a way to counter the interference issues with AM radio. 
               FM radio stations happen on VHF (very high frequency) waves and are much more common thanks to better sound fidelity,
               which allowed stereo broadcasting to thrive on this specific type of broadcasting network. FM ranges are much shorter 
               than AM ranges, despite the high frequency, meaning that the FM radio is much better for local radio stations. When this 
               technology was originally developed, there wasn’t a specific threat to AM radio because FM radio required people to purchase 
               a different type of receiver. It wasn’t until after World War II that there was a change in frequencies that allowed FM radio 
               to be more accessible, thus toppling the popularity of AM radio.
        </h4></p>
        <p><h4>
        (3)Pirate Radio-This type of radio refers to ‘illegal’ or un-regulated radio transmission. Often, this sort of radio broadcasting is used for political reasons,
                        for entertainment, or as an exchange of two-way radio. While the origins of pirate radio can be linked to a general unlicenced use, history suggests 
                        that there have been moments that ships out at sea have used this mode of communication (ie pirate) as a base for broadcasting. While rules and regulations 
                        depend on where a person is at in the world, this type of broadcasting normally refers to an FM, AM, or short wave signal transmission. Even more interesting 
                        is that the origin of the broadcast may be legal, but where the broadcast is received may be construed as illegal (ie if a broadcast goes across borders).
        </h4></p>
       </div>
     

