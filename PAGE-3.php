                    <p><h4>During World War One, a Committee on Public Information ("Compub"), headed by George Creel, was formed to promote the United States war effort at home and abroad. 
                           In the December, 1918 Illustrated World, Donald Wilhelm recounted how the high-powered radio stations of the United States Navy were used for Sending the News to 
                           the Seven Seas -- "broadcasting the truth and the light", as provided "by the most powerful educational institution in the world, the Committee on Public Information".
                           In the September, 1922 Popular Radio, Creel's The Battle in the Air Lanes noted that the World War had been the first to include "Public Opinion as a major force" where 
                           "moral verdicts took on all the value of military decisions", and the United States, with limited access to the international cables, had turned to radio for "reaching 
                           every country on the globe with the American message". A short reference in the February 21, 1919 Boston Herald noted that the American Radio and Research Corporation of Medford, 
                           Massachusetts, in conjunction with Major Foster Veitenheimer, signal officer of the department of the northeast, was planning to "send greetings" to President Wilson aboard the 
                           George Washington as the vessel approached Boston for a planned visit.
                    </h4></p></div> 
                    
                    
