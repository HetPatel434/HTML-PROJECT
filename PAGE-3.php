                            <p><h4>While radio remained off-limits for the general public during the war, there were occasional hints of what lay ahead. Wireless Music for Wounded Soldiers from the April,
                                   1918 The Wireless Age reviewed a short-range electrostatic induction system that could be used to entertain hospitalized soldiers with music and news. And between the 
                                   cessation of hostilities in November, 1918, and the end of the civilian radio restrictions in 1919, there were scattered reports of military personnel firing up transmitters
                                   in order to broadcast entertainment to the troops -- for example, the January 10, 1919 Springfield Republican reported that Ensign Sanford Lawton Had Christmas at Sea, and in
                                   his letter to his parents noted that, as part of the festivities, "All of the latest music from the states was played over the wireless." A short time later Lieut. R. C. Roberts,
                                   of the Rockwell field radio department, transmitted a "Moonlight Wireless Dance", featuring "an abundance of entertainment for all", from the Battleship Marblehead, off the coast
                                   of San Diego, California, to dancers in Hanger Number 4, as reported in Wireless Phone to Transmit Music for Dance from the February 1, 1919, San Diego Evening Tribune, and Music 
                                   by Wireless, in the March, 1919 issue of Telephone Engineer. In addition, the May 7, 1919 Dallas Morning News reported that U.S.S. George Washington, during its transatlantic crossing, 
                                   had employed its radio transmitter to provide nightly a Concert by Wireless for Vessels at Sea.</h4></p></div>    

