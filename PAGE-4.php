                    <p><h4>
                    According to Owen D. Young, the General Electric Company executive who coordinated G.E.'s purchase of American Marconi, and its transformation into the Radio Corporation of America:
                        "Fifteen years is the average period of probation, and during that time the inventor, the promoter and the investor, who see a great future, generally lose their shirts... This is why
                         the wise capitalist keeps out of exploiting new inventions and comes in only when the public is ready for mass demand". When, after years of losing money, radio finally started to become
                         profitable in the late teens, then grew explosively with the broadcasting boom in the early twenties, the "wise capitalists" at major industrial corporations like G.E. began to enter and 
                         dominate the industry, in particular by buying up most of the major patents. In contrast, after nearly two decades of pioneering work and struggling companies, in 1921 Lee DeForest abruptly 
                         sold most of his radio interests and moved on to other fields. DeForest later explained that he felt the time had come when "the building up of this technique and institution might better 
                         be left in the hands of those with greater capital, influence and personnel to carry on" and further noted that broadcasting "grew amazingly, once the large organizations with ample capital 
                         took hold of it".</p></h4></div> 
                    
                    
