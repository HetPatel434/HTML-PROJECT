                            <p><h4>The Westinghouse Electric & Manufacturing Company, based in East Pittsburgh, Pennsylvania, also became one of the expanding radio industry's most prominent leaders. Westinghouse was 
                                   a major, and well-respected, manufacturer of electrical appliances for the home, and would become the first company to broadly market radio receivers to the general public. Although 
                                   the company had been involved in radio research to a limited degree during World War One, after the war Westinghouse began to greatly extend its operations, including the purchase of 
                                   the International Radio Telegraph Company -- the struggling successor to Fessenden's National Electric Signaling Company (NESCO). In 1912, following a losing initial decision in a 
                                   lawsuit initiated by Reginald Fessenden, NESCO had gone into receivership, reported in Marconi Competitor Bankrupt from the August 6, 1912 Wall Street Journal. During World War One, 
                                   NESCO exited bankruptcy and was renamed International Radio Telegraph -- its subsequent purchase was reported in Westinghouse Company Enters Wireless Field from the October 16, 1920 
                                   Electrical Review. This article noted that "special attention would be paid to the development of new uses" of radio, and the very next month, inspired by Frank Conrad's broadcasts 
                                   over 8XK, Westinghouse inaugurated a public broadcasting service, designed to promote the sale of radio receivers. The October 28, 1920 Cleveland Plain Dealer announced that the formal 
                                   start of the new service would be To Give Election Returns by Radio on November 2, 1920, from a station located at Westinghouse's main plant in East Pittsburgh, Pennsylvania.</h4></p></div>     

